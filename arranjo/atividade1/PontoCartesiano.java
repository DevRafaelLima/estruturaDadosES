package arranjo.atividade1;

public class PontoCartesiano {
    private float X;
    private float Y;

    public PontoCartesiano(float X, float Y){
        this.X = X;
        this.Y = Y;
    }

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }
    

}
